package com.stackroute.numberandstrings;


public class NumberClass {
    public static void main(String[] args) {
        Integer i = 374;

        // Converting into byte value
        System.out.println("Byte value is "+i.byteValue());
        // Converting into double value
        System.out.println("Double value is "+i.doubleValue());
        // Converting into float value
        System.out.println("Float value is "+i.floatValue());
        // Converting into long value
        System.out.println("Long value is "+i.longValue());
        // Converting into string value
        System.out.println("String value is "+i.toString());

    }
}
